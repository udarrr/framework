public class EstimateResult {
    public String vmClassField;
    public String instanceTypeField;
    public String regionField;
    public String localSSDField;
    public String commitmentTermField;

    public String getVmClassField() {
        return vmClassField;
    }

    public void setVmClassField(String vmClassField) {
        this.vmClassField = vmClassField;
    }

    public String getInstanceTypeField() {
        return instanceTypeField;
    }

    public void setInstanceTypeField(String instanceTypeField) {
        this.instanceTypeField = instanceTypeField;
    }

    public String getRegionField() {
        return regionField;
    }

    public void setRegionField(String regionField) {
        this.regionField = regionField;
    }

    public String getLocalSSDField() {
        return localSSDField;
    }

    public void setLocalSSDField(String localSSDField) {
        this.localSSDField = localSSDField;
    }

    public String getCommitmentTermField() {
        return commitmentTermField;
    }

    public void setCommitmentTermField(String commitmentTermField) {
        this.commitmentTermField = commitmentTermField;
    }
}
